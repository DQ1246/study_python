import re
s="12345678"
s1="12345678910"
s2="123sajd"
s3="ssad2.ssda.a@qq.com"
s4="sasd-1.ssad@gmail.com"
s5="sadA1s.asda.sa@163.com.cn"
#账号，字母与数字组成，长度6-10
r='^[0-9a-zA-Z]{6,10}$'#对全字符串匹配
result=re.findall(r,s)
result1=re.findall(r,s1)
result2=re.findall(r,s2)
print("账号")
print(result)
print(result1)
print(result2)
#QQ号，首位不为零，纯数字，长度5-11
print("qq号")
r1='^[1-9][0-9]{4,10}$'
result3=re.findall(r1,s)
result4=re.findall(r1,s1)
result5=re.findall(r1,s2)
print(result3)
print(result4)
print(result5)
#邮箱，满足qq、163、gmail三种邮箱
print("邮箱")
r2=r'^([\w_-]+(\.[\w_-]+)*@(qq|gmail|163)(\.[\w_-]+)+$)'#findall会对（）作为对象进行返回，最终用括号全部阔起来，保证信息完整
result6=re.findall(r2,s3)#单个字符就是一个匹配项，无论挨近还是靠远
result7=re.findall(r2,s4)
result8=re.findall(r2,s5)
print(result6)
print(result7)
print(result8)